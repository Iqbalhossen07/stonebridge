        const textItems = document.querySelectorAll('.benefit-text-item');
        const imageContainer = document.getElementById('image-container');
        const images = document.querySelectorAll('.benefit-image');

        if (textItems.length > 0 && imageContainer && images.length > 0) {
            const setActiveImage = (imageId) => {
                images.forEach(img => {
                    if (img.id === imageId) {
                        img.classList.add('opacity-100');
                        img.classList.remove('opacity-0');
                    } else {
                        img.classList.add('opacity-0');
                        img.classList.remove('opacity-100');
                    }
                });
            };
            setActiveImage('image-1');
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        const imageId = entry.target.getAttribute('data-image-id');
                        setActiveImage(imageId);
                    }
                });
            }, {
                rootMargin: '-50% 0px -50% 0px',
                threshold: 0
            });
            textItems.forEach(item => {
                observer.observe(item);
            });
        }
    });
